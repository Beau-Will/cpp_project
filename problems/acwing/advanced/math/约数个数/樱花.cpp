using Z = MInt<P1>;

std::vector<int> minp, primes;

void sieve(int n) {
  minp.assign(n + 1, 0);  // 初始所有数的最小质因数为0（表示未处理）
  primes.clear();         // 清空质数列表

  for (int i = 2; i <= n; i++) {
    if (minp[i] == 0) {          // i是质数
      minp[i] = i;            // 质数的最小质因数是它自己
      primes.push_back(i);    // 加入质数列表
    }

    // 用当前数i去乘已发现的质数
    for (auto p : primes) {
      if (i * p > n) {   // 超出范围则终止
        break;
      }
      minp[i * p] = p;        // 标记i*p的最小质因数是p
      if (p == minp[i]) {// 关键：如果p是i的最小质因数，则跳出循环
        break;
      }
    }
  }
}

bool isprime(int n) {
  return minp[n] == n;
}

void solve(){
  int n;
  std::cin >> n;

  sieve(n);

  std::map<int,int> cnt;
  for(const auto& p:primes){
    i64 t = p;
    while(t <= n){
      cnt[p] += n/t;
      t *= p;
    }
  }

  Z ans = 1;
  for(const auto& [u,v]:cnt){
    ans *= (v*2+1);
  }

  std::cout << ans << "\n";
}

